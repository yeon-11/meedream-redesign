<script setup>
import { ref, onMounted } from "vue";

import aftercareMobile from '@/assets/img/breast/aftercare.png'
import aftercareTablet from '@/assets/img/breast/aftercare_tb.png'
import aftercarePC from '@/assets/img/breast/aftercare_pad.png'

const screenWidth = ref(window.innerWidth);

function getResponsiveImage() {
  if (screenWidth.value <= 480) {
    return aftercareMobile;
  } else if (screenWidth.value <= 768) {
    return aftercareTablet;
  } else {
    return aftercarePC;
  }
}

onMounted(() => {
  window.addEventListener("resize", () => {
    screenWidth.value = window.innerWidth;
  });
});
</script>


